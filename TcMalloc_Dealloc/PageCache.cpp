#include"PageCache.h"

PageCache PageCache:: _sInstan;
Span* PageCache::NewSpan(size_t k)
{
	assert(k > 0 && k < NPAGES);
	if (!_spanLists[k].Empty())
	{
		return _spanLists[k].PopFront();
	}
	//检查后面的桶里面有没有span。如果有可以把他进行切分
	for (size_t i = k + 1; i < NPAGES; ++i)
	{
		if (!_spanLists[i].Empty())
		{
			//只要有不为空的桶，就进行切分:把n页的span切分成k页的span和n-k页的span；k页的
			//span返回给centralcache；n-k也的span挂到第n-k桶中去
			Span* nSpan = _spanLists[i].PopFront();
			Span* kSpan = new Span;
			//在nSpan的头部切一个k页下来
			kSpan->_pageId = nSpan->_pageId;
			kSpan->_n = k;

			nSpan->_pageId += k;
			nSpan->_n -= k;

			_spanLists[nSpan->_n].PushFront(nSpan);
			//存储nSpan的首尾页号跟nSpan的映射，方便page cache回收内存时进行合并查找
			_idSpanMap[nSpan->_pageId] = nSpan;
			_idSpanMap[nSpan->_pageId + nSpan->_n - 1] = nSpan;

			//建立id和span的映射，方便centralcache回收小块内存时查找对应的span
			for (PAGE_ID i = 0; i < kSpan->_n; ++i)
			{
				_idSpanMap[kSpan->_pageId + i] = kSpan;
			}
			return kSpan;
		}
	}
	//后面没有大页的span->找堆要一个128页的span
	Span* bigSpan = new Span;
	void* ptr = SystemAlloc(NPAGES - 1);
	//大页内存的页号=大页内存的起始地址/单页的大小
	bigSpan->_pageId = (PAGE_ID)ptr >> PAGE_SHIFT;
	bigSpan->_n = NPAGES - 1;

	_spanLists[bigSpan->_n].PushFront(bigSpan);
	return NewSpan(k);
}
Span* PageCache::MapObjectToSpan(void* obj)
{
	PAGE_ID id = ((PAGE_ID)obj >> PAGE_SHIFT);
	auto ret = _idSpanMap.find(id);
	if (ret != _idSpanMap.end())
	{
		return ret->second;
	}
	else
	{
		assert(false);
		return nullptr;
	}
}

void PageCache::ReleaseSpanToPageCache(Span* span)
{
	//对span前后的页，尝试进行合并，缓解内存碎片问题
	while (true)
	{
		PAGE_ID prevId = span->_pageId - 1;
		auto ret = _idSpanMap.find(prevId);
		//span前面的页号没有，不合并
		if (ret == _idSpanMap.end())
		{
			break;
		}
		//前面相邻的页的span在被使用，不合并
		Span* prevSpan = ret->second;
		if (prevSpan->_isUse)
		{
			break;
		}
		//合并的总页数超过NPAGES-1，不合并
		if (prevSpan->_n + span->_n > NPAGES - 1)
		{
			break;
		}
		span->_pageId = prevSpan->_pageId;
		span->_n += prevSpan->_n;
		_spanLists[prevSpan->_n].erase(prevSpan);
		delete prevSpan;
	}
	//向后合并
	while (true)
	{
		PAGE_ID nextId = span->_pageId +span->_n;
		auto ret = _idSpanMap.find(nextId);
		//span前面的页号没有，不合并
		if (ret == _idSpanMap.end())
		{
			break;
		}
		//前面相邻的页的span在被使用，不合并
		Span* nextSpan = ret->second;
		if (nextSpan->_isUse)
		{
			break;
		}
		//合并的总页数超过NPAGES-1，不合并
		if (nextSpan->_n + span->_n > NPAGES - 1)
		{
			break;
		}
		span->_n += nextSpan->_n;
		_spanLists[nextSpan->_n].erase(nextSpan);
		delete nextSpan;
	}
	//挂在对应的spanlists，并在_idSpanMap中填充映射规则
	_spanLists[span->_n].PushFront(span);
	span->_isUse = false;
	_idSpanMap[span->_pageId] = span;
	_idSpanMap[span->_pageId + span->_n - 1] = span;
}